public class operadoresAratimeticos {
  public static void main(String[] args) {
    // Operadores Aritm√©ticos: +, -, *, /, %
    double nunber1 = 10;
    double nunber2 = 3;
    double sum = nunber1 + nunber2;
    double substract = nunber1 - nunber2;
    double multiply = nunber1 * nunber2;
    double divide = nunber1 / nunber2;
    double module = nunber1 % nunber2;

    System.out.println("Sum is: " + sum);
    System.out.println("Substract is: " + substract);
    System.out.println("Multiply is: " + multiply);
    System.out.println("Divide is: " + divide);
    System.out.println("Module is: " + module);
  }
}