package primitivos;

public class PrimitiveTypes {
    public static void main(String[] args) {
        //int, double, float, char, byte, short, git long, boolean
        int age = 10;
        float salary = (float) 2000.0D;
        char character = 'M'; // only character for time
        System.out.println(age);
        System.out.println(salary);
        System.out.println("char "+character);
    }
}
